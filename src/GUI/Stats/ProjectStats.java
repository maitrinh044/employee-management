    private void createGenderPieChart(int[] genderStats) {
        // Lấy dữ liệu thống kê giới tính
        
        int active = genderStats[0];
        int inactive = genderStats[1];

        // Tính toán phần trăm
        int totalCount = active + inactive;
        double activePercent = (double) active / totalCount * 100;
        double inactivePercent = (double) inactive / totalCount * 100;

        // Tạo dataset cho biểu đồ hình tròn
        DefaultPieDataset dataset = new DefaultPieDataset();
        dataset.setValue("Đang hoạt động", active);
        dataset.setValue("Đã hoàn thành", inactive);

        // Tạo biểu đồ hình tròn
        JFreeChart pieChart = ChartFactory.createPieChart(
                "Thống kê dự án hoạt động", // Tiêu đề biểu đồ
                dataset, // Dataset
                true, // Hiển thị legend
                true, // Hiển thị tooltips
                false // Không hiển thị URL
        );

        // Tùy chỉnh các đặc tính của PiePlot
        PiePlot plot = (PiePlot) pieChart.getPlot();
        plot.setSectionPaint("Đang hoạt động", new Color(0x42A5F5)); 
        plot.setSectionPaint("Đã hoàn thành", new Color(0xF06292)); 

        // Tùy chỉnh cách hiển thị thông tin phần trăm
        plot.setLabelGenerator(new StandardPieSectionLabelGenerator(
                "{0}: {2}%"));  // {0} là tên phần, {2} là phần trăm

        // Tùy chỉnh các tooltips hiển thị phần trăm
        plot.setToolTipGenerator(new StandardPieToolTipGenerator(
                "{0}: {2}%"));  // {0} là tên phần, {2} là phần trăm

        // Tuỳ chỉnh legend
        pieChart.getLegend().setPosition(RectangleEdge.BOTTOM);  // Đặt legend ở dưới
        pieChart.getLegend().setItemFont(new Font("Segoe UI", Font.PLAIN, 12));  // Đặt font cho legend

        // Tạo ChartPanel từ biểu đồ pieChart
        ChartPanel chartPanel = new ChartPanel(pieChart);
        chartPanel.setPreferredSize(new java.awt.Dimension(500, 300));  // Đặt kích thước cho biểu đồ
        chartPanel.setMouseWheelEnabled(true);  // Kích hoạt zoom với chuột
        chartPanel.setPopupMenu(null);          // Ẩn menu chuột phải

        // Thêm biểu đồ vào jPanel5
        jPanel5.add(chartPanel, BorderLayout.CENTER);

        // Cập nhật lại layout
        revalidate();
        repaint();
    }

